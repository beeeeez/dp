package ex8_2;

public class GarageDoor {

	public void up() {
		System.out.println("the gd is open");
	}

	public void down() {
		System.out.println("the gd is close");
	}

	public void stop() {
		System.out.println("the gd is stopped");
	}

	public void lightOn() {
		System.out.println("the gd light is on");
	}

	public void lightOff() {
		System.out.println("the gd light is off");
	}
	
}
